#include<iostream>
using namespace std;
int main()
{
    /*Float, Double and Literal data types*/
    float a = 34.4f;
    long double b = 34.4l;
    cout<<"Size of 34.4 is "<<sizeof(34.4)<<endl;
    cout<<"Size of 34.4f is "<<sizeof(34.4f)<<endl;
    cout<<"Size of 34.4l is "<<sizeof(34.4l)<<endl;
    cout<<"Size of 34.4F is "<<sizeof(34.4F)<<endl;
    cout<<"Size of 34.4L is "<<sizeof(34.4L)<<endl;
    return 0;
}